    private void findAllAttendance() {
        DefaultTableModel dtm = (DefaultTableModel) tblAttendance.getModel();
        dtm.setRowCount(0);
        try {
            AttendanceController objAttendanceController = new AttendanceController();
            List<Attendance> attendance = objAttendanceController.findAllAttendance();

            for (int i = 0; i < attendance.size(); i++) {
                Attendance e = attendance.get(i);
                Object[] data = {e.getAttendance_id(), e.getEmployee_id(), e.getFullName(),e.getIn_time(), e.getOut_time(),  e.getAttendance_date()};
                dtm.addRow(data);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
