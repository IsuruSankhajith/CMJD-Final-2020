    private void tblAttendanceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAttendanceMouseClicked
        try {
            int row = tblAttendance.getSelectedRow();
            int attendanceID = 0;
            Attendance attendance = new Attendance();

            if (row >= 0) {
                attendanceID = Integer.parseInt(tblAttendance.getModel().getValueAt(row, 0).toString());
                attendance.setAttendance_id(attendanceID);
                attendance.setIn_time(Timestamp.valueOf(tblAttendance.getModel().getValueAt(row, 3).toString()));
                attendance.setOut_time(Timestamp.valueOf(tblAttendance.getModel().getValueAt(row, 4).toString()));
                attendance.setEmployee_id(Integer.parseInt(tblAttendance.getModel().getValueAt(row, 1).toString()));
            }
            UpdateAttendanceDetailsForm updateAttendanceDetailsForm = new UpdateAttendanceDetailsForm(attendance);
            updateAttendanceDetailsForm.setVisible(true);
            updateAttendanceDetailsForm.setLocationRelativeTo(null);

        } catch (Exception ex) {
            Logger.getLogger(EmployeeListForm.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tblAttendanceMouseClicked
    private void LoadCombo() {
        try {
            EmployeeController employeeController = new EmployeeController();
            List<Employee> employeeList = employeeController.findAllEmployee();
            cmbEmployee.removeAllItems();

            for (int i = 0; i < employeeList.size(); i++) {
                Employee employee = employeeList.get(i);
                cmbEmployee.addItem(employee.getEmployeeId().toString());
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

