    private void findAllEmployees() {
        DefaultTableModel dtm = (DefaultTableModel) tblEmployees.getModel();
        dtm.setRowCount(0);
        try {
            EmployeeController objEmployeeController = new EmployeeController();
            List<Employee> employees = objEmployeeController.findAllEmployeeWithCategoryAndType();

            for (int i = 0; i < employees.size(); i++) {
                Employee e = employees.get(i);
                Shift shift = e.getShiftObj();
                Object[] data = {e.getEmployeeId(), e.getFirstName(), e.getLastName(), e.getContact(), e.getNic(), e.getAddress(), shift.getShift_name()};
                dtm.addRow(data);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

