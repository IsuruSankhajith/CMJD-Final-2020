    private void UpdateEmployeeFormActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateEmployeeFormActionPerformed
        try {
            int row = tblEmployees.getSelectedRow();
            int employeeID = 0;
            if (row >= 0) {
                employeeID = Integer.parseInt(tblEmployees.getModel().getValueAt(row, 0).toString());
            }
            Employee employee = new Employee();
            employee.setEmployeeId(employeeID);
            EmployeeController objEmployeeController = new EmployeeController();
            List<Employee> employees = objEmployeeController.findEmployeeDetailsForId(employee);
            Employee e = new Employee();
            for (int i = 0; i < employees.size(); i++) {
                e = employees.get(i);
            }
            UpdateEmployeeForm updateEmployeeForm = new UpdateEmployeeForm(e);
            updateEmployeeForm.setVisible(true);
            updateEmployeeForm.setLocationRelativeTo(null);

        } catch (Exception ex) {
            Logger.getLogger(EmployeeListForm.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_UpdateEmployeeFormActionPerformed

