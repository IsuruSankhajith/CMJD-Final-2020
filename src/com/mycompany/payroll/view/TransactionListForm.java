    private void findAllTransaction() {
        DefaultTableModel dtm = (DefaultTableModel) tblTransactionHeader.getModel();
        dtm.setRowCount(0);
        try {
            TransactionController transactionController = new TransactionController();
            List<Transaction> lstTransactions = transactionController.findAllTransaction();

            for (int i = 0; i < lstTransactions.size(); i++) {
                Transaction transaction = lstTransactions.get(i);
                Object[] data = {transaction.getTransaction_id(), transaction.getEmployee_id(), transaction.getGross_salary(), transaction.getNet_salary(), transaction.getSalary_for(), transaction.getTransaction_time()};
                dtm.addRow(data);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
