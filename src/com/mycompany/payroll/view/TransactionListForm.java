    private void tblTransactionHeaderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransactionHeaderMouseClicked
        try {
            int row = tblTransactionHeader.getSelectedRow();
            int transactionId = 0;
            if (row >= 0) {
                transactionId = Integer.parseInt(tblTransactionHeader.getModel().getValueAt(row, 0).toString());
            }
            TransactionDetail transactionDetail = new TransactionDetail();
            transactionDetail.setTransaction_id(transactionId);
            TransactionDetailController transactionDetailController = new TransactionDetailController();
            List<TransactionDetail> lsttTransactionDetails = transactionDetailController.findTransactionDetailsForId(transactionDetail);
             
            DefaultTableModel dtmDetail = (DefaultTableModel) tblTransactionDetail.getModel();
            dtmDetail.setRowCount(0);
            
            TransactionDetail e = new TransactionDetail();
            for (int i = 0; i < lsttTransactionDetails.size(); i++) {
                e = lsttTransactionDetails.get(i);
                Object[] data = {e.getTransaction_detail_id(), e.getTransaction_id(), e.getDescription(), e.getAmount()};
                dtmDetail.addRow(data);
            }

        } catch (Exception ex) {
            Logger.getLogger(EmployeeListForm.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tblTransactionHeaderMouseClicked


